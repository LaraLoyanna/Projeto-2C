package CANTINA;

public class Bebida {
    private Integer QuantidadeDeBebida;
    private String TamanhoDaBebida;
    private Integer TipoDaBebida;

    public Integer getQuantidadeDeBebida() {
        return QuantidadeDeBebida;
    }

    public void setQuantidadeDeBebida(Integer quantidadeDeBebida) {
        QuantidadeDeBebida = quantidadeDeBebida;
    }

    public String getTamanhoDaBebida() {
        return TamanhoDaBebida;
    }

    public void setTamanhoDaBebida(String tamanhoDaBebida) {
        TamanhoDaBebida = tamanhoDaBebida;
    }

    public Integer getTipoDaBebida() {
        return TipoDaBebida;
    }

    public void setTipoDaBebida(Integer tipoDaBebida) {
        TipoDaBebida = tipoDaBebida;
    }
}
